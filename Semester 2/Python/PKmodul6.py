import turtle as t
t.speed(5)
def kotak(panjang,level):
    warna = ['blue','red','green','purple']
    idx = level % 4
    if level == 0:
        t.begin_fill()
        t.color('black',warna[idx])
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.end_fill()
    else:
        t.begin_fill()
        t.color('black',warna[idx])
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.forward(panjang)
        t.right(90)
        t.end_fill()
        t.forward(panjang/2)
        t.right(90)
        t.forward(panjang/2)
        t.right(180)
        kotak(panjang/2,level-1)

kotak(200,3)
t.done()